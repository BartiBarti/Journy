<p>A personal, face-to-face encounter with this awesome animal, is guaranteed to get the adrenaline going! Dive with the sharks
    in Gaansbaai! Getting into the cage with the sharks around, is truly one of the most breath-taking adventure activities
    you will ever live to remember. When feeding actively around the boat, the sharks may occasionally brush their tail against
    the cage, but they NEVER attack the cage. They are very curious though and often come close-up to the cage, to take a
    closer look at the diver.
